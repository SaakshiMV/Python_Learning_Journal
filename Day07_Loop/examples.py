# Example 1: for loop
print("\n=====================================")
for i in range(5):
    print("Hello Python")


# Example 2: print numbers 1–10
print("\n=====================================")
for i in range(1, 11):
    print(i)


# Example 3: while loop
print("\n=====================================")
count = 1
while count <= 5:
    print("Count:", count)
    count += 1


# Example 4: simple sum
print("\n=====================================")
total = 0
for i in range(1, 6):
    total += i

print("Sum:", total)
