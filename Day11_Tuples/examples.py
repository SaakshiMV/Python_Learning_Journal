# Creating tuples
numbers = (1, 2, 3, 4)
fruits = ("apple", "banana", "mango")

# Accessing elements
print(fruits[0])
print(fruits[1])

# Length of tuple
print(len(numbers))

# Looping through tuple
for fruit in fruits:
    print(fruit)

# Tuple with mixed data types
data = ("xyz", 30, True)
print(data)
