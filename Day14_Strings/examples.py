# Creating strings
text = "Python Programming"

# Accessing characters
print(text[0])
print(text[7])

# Slicing
print(text[0:6])
print(text[7:])

# String methods
print(text.upper())
print(text.lower())
print(text.replace("Python", "Java"))

# Checking
print(text.isalpha())
print(text.isalnum())

# Length
print(len(text))
