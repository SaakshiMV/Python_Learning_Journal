# Creating lists
fruits = ["apple", "banana", "mango"]
numbers = [10, 20, 30]


# Accessing elements
print(fruits[0])
print(fruits[1])


# Modifying list
fruits[1] = "orange"
print(fruits)


# Adding items
numbers.append(40)
print(numbers)


# Removing items
fruits.remove("apple")
print(fruits)


# Looping through list
for fruit in fruits:
    print(fruit)
